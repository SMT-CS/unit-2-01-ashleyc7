import math


def main():
    """
    The main() function calculates the circumference of a circle with the diameter of 42 mm,
    """

    print("If a circle has the diameter of 42 mm,")
    print(f"the circumference is  {(math.pi  * 42):.5f} mm.")

    print("\nDone.")


if __name__ == "__main__":
    main()
